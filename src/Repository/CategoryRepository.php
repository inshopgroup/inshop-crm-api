    /**
     * @return array
     */
    public function getCategories(): array
    {
        return $this->createQueryBuilder('c')
            ->select('c', 'sc')
            ->leftJoin('c.subCategories', 'sc')
            ->andWhere('c.parent is null')
            ->andWhere('c.isActive = true')
            ->orderBy('c.position', 'desc')
            ->getQuery()
            ->getResult()
        ;
    }
